local opt = vim.opt

-- Line numbers
opt.number = true
opt.relativenumber = true

-- Indentation
opt.expandtab = true
opt.shiftwidth = 4
opt.tabstop = 4
opt.smartindent = true

-- Searching
opt.ignorecase = true
opt.smartcase = true

-- Scrolling
opt.scrolloff = 8

-- Appearance
opt.termguicolors = true
opt.signcolumn = "yes"

-- Mouse
opt.mouse = ""

-- Clipboard
opt.clipboard = "unnamedplus"

-- Undo
opt.undofile = true

-- Splits
opt.splitright = true
opt.splitbelow = true

-- Wrapping
opt.wrap = false

-- Update speed
opt.updatetime = 250
